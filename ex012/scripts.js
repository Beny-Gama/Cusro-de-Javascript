//Operadores Igualdade
// No JS Temos 2 tipos de operadores de iquandade.

// Iqualdade Restrita:
console.log(1 === 1);
console.log('1' === 1);
// Retorna o Tipagem Dinamica da variavel

// Iqualdade Solta:
console.log (1 == 1);
console.log ('1' == 1);
// tenta iqualar o Tipagem Dinamica  mais adequada da variavel

