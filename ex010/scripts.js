// Operadores Aritimeticos (matematicos)
// soma == + 
// subtração== -
// Multiplicão == * 
// Divisão == /
// Esponencial == **

let salario = 10;

console.log(salario + salario);
console.log(salario - salario);
console.log(salario * salario);
console.log(salario / salario);
console.log(salario ** salario);


// emcremento == ++
